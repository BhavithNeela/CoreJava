package task;

public class Swapping {

	public static void main(String[ ] args) {
		int a =10 ,b = 20 , c= 30;
		System.out.println("before swapping");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		a=b;
		b=c;
		c=a;
		System.out.println("after swapping");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
	}
}
